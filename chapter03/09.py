# 3-9. Dinner Guests: Working with one of the programs from Exercises 3-4
# through 3-7 (pages 41–42), use len() to print a message indicating the number
# of people you’re inviting to dinner.

guests = ["alice", "bob", "carol"]

print(f"Would you like to my dinner, {guests[0]}?")
print(f"Would you like to my dinner, {guests[1]}?")
print(f"Would you like to my dinner, {guests[2]}?")

print(f"Great. {len(guests)} people will come to my dinner.")
